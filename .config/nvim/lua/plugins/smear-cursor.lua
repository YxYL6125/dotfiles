-- ~/.config/nvim/lua/user/plugins/smear_cursor.lua (或者您放置该配置的文件)
return {
  "sphamba/smear-cursor.nvim",
  event = "VeryLazy",

  opts = {
    -- 您原有的配置，保持开启
    smear_between_buffers = true,
    scroll_buffer_space = true,
    smear_insert_mode = true,
    legacy_computing_symbols_support = false,

    -- ======================================================
    -- ============== 1. 核心视觉定制 (`scheme`) ==============
    -- ======================================================
    -- 这是最重要的定制选项！它定义了整个动画的色彩和字符主题。
    -- 默认是 "sand"。您可以尝试 "fire", "ice", "matrix", "trail" 等。
    scheme = "trail",

    -- ======================================================
    -- ================== 2. 动画速度与效果 ===================
    -- ======================================================
    -- 动画的总持续时间（毫秒）。减小它会让动画更快、更干脆。
    animation_duration = 100, -- 默认是 500

    -- ======================================================
    -- ================== 3. 触发条件优化 ===================
    -- ======================================================
    -- 原来的 `smear_between_neighbor_lines` 有时会过于频繁。
    -- 使用下面的选项可以进行更精细的控制：
    --
    -- 只有当垂直移动超过 N 行时，才触发动画。
    -- 设置为 2 表示上下移动 1 行不会有动画，但移动 2 行或以上就会有。
    -- 这可以避免在逐行阅读代码时产生不必要的干扰。
    min_vertical_distance_smear = 2,

    -- (同理) 只有当水平移动超过 N 列时，才触发动画。
    min_horizontal_distance_smear = 5,

    -- ======================================================
    -- ================== 4. 插入模式优化 ====================
    -- ======================================================
    -- 在插入模式下，让光标保持为竖线形状进行动画，而不是一个方块。
    -- 这在视觉上更自然，强烈建议开启。
    vertical_bar_cursor_insert_mode = true,

    -- ======================================================
    -- ================== 5. 性能与兼容性 ===================
    -- ======================================================
    -- 在以下文件类型中禁用动画，以避免与特殊插件窗口冲突。
    exclude_filetypes = {
      "TelescopePrompt",
      "lazy",
      "mason",
      "neo-tree",
      "notify",
      "toggleterm",
    },
    -- 在以下 buffer 类型中禁用动画。
    exclude_buftypes = { "terminal", "nofile", "quickfix" },
  },
}
